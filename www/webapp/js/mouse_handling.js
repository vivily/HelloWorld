function unbindMouseEvents()
{
	if (window.console) console.log("unbindMouseEvents");
   $('#svgintro3').unbind('mousedown');
   $('#svgintro3').unbind('mousemove');
   $('#svgintro3').unbind('mouseup');
}

function unbindKeypress()
{
	if (window.console) console.log("unbindKeypress called");
	$('#svgintro3').unbind('keypress');
	document.onkeypress = null;
}
